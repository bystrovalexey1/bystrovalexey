def test_filter_by_currency(transaction_list: list[dict], usd_transaction: list[dict]) -> None:
    """Проверка фильтрации списка по ключу валюты"""
    assert list(filter_by_currency(transaction_list, "USD")) == usd_transaction


def test_filter_by_currency_empty_or_noone(transaction_list_without_usd: list[dict]) -> None:
    """Проверка выдачи пустого списка, если нет операций в заданной валюте или пустого списка"""
    assert list(filter_by_currency(transaction_list_without_usd)) == []
    assert list(filter_by_currency([])) == []


def test_transaction_descriptions(transaction_list: list[dict]) -> None:
    """Функция тестирует выдачу списка описания операций"""
    des = transaction_descriptions(transaction_list)
    assert next(des) == "Перевод организации"
    assert next(des) == "Перевод со счета на счет"
    assert next(des) == "Перевод со счета на счет"
    assert next(des) == "Перевод с карты на карту"


def test_transaction_descriptions_empty() -> None:
    """Функция тестирует вывод при пустом списке"""
    descriptions = transaction_descriptions([])
    assert list(descriptions) == []


@pytest.mark.parametrize("start, end", [(1, 3)])
def test_card_number_generator(start: int, end: int) -> None:
    """Функция тестирует генератор номеров карт"""
    gen_number = card_number_generator(start, end)
    assert next(gen_number) == "0000 0000 0000 0001"
    assert next(gen_number) == "0000 0000 0000 0002"
    assert next(gen_number) == "0000 0000 0000 0003"
